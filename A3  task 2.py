import math

# Ask user for input
num = float(input("Enter a number: "))

# Perform calculations using math module
square_root = math.sqrt(num)
natural_log = math.log(num)  # log base e
sine_value = math.sin(num)   # num is in radians

# Display results
print(f"\nResults for the number {num}:")
print(f"Square Root: {square_root}")
print(f"Natural Logarithm (log base e): {natural_log}")
print(f"Sine (in radians): {sine_value}")








